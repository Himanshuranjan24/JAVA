
package calculator;
import java.lang.*;
import java.util.*;
public class CALC {
    public int add(int a, int b) {
        return a + b;
    }
    public  int subtract(int a, int b) {
        return a - b;
    }
    public int multiply(int a, int b) {
        return a * b;
    }
     public int divide(int a, int b) {
        return a / b;
    }
     public int mod(int a, int b) {
        return a % b;
    }
}

